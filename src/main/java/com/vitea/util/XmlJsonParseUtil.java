package com.vitea.util;
/**
 * 处理xml json相互转换
 * @author liushahe
 *
 */
public class XmlJsonParseUtil {
	
	/**
	 * xml转json
	 * @param xml
	 * @return
	 */
	public static String XmlToJson(String xml) {
		return xml;
		
	}
	/**
	 * json转xml
	 * @param json
	 * @return
	 */
	public static String JsonToXml(String json) {
		return json;
		
	}
}
